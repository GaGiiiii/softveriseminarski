    private void pretraziBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pretraziBtnActionPerformed
        try {

            String nazivf = naziv.getText();
            LinkedList<IDomain> filmovi = new LinkedList<>();

            Request req = new Request();
            req.setOperation(Operation.PRETRAZI_FILMOVE);
            req.setParameter(new PretragaHelp(nazivf, filmovi));

            CommunicationWithServer.getInstance().sendRequest(req);
            Response res = CommunicationWithServer.getInstance().getResponse();

            if (res.getException() == null) {
                tableModel = new FilmTableModel((List<Film>) res.getResponse());
                filmoviTable.setModel(tableModel);
            } else {
                JOptionPane.showMessageDialog(this, res.getException().getMessage(), "Neuspesno pretrazivanje filmova", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska prilikom pretrazivanja", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }

    }//GEN-LAST:event_pretraziBtnActionPerformed

