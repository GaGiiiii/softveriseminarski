    private void init() {
        obrisiBtn.setEnabled(false);
        try {
            movieInfoPanel.setVisible(false);

            Request req = new Request();
            req.setOperation(Operation.UCITAJ_LISTU_FILMOVA);

            CommunicationWithServer.getInstance().sendRequest(req);
            Response res = CommunicationWithServer.getInstance().getResponse();
            LinkedList<Film> filmovi = (LinkedList<Film>) res.getResponse();

            tableModel = new FilmTableModel(filmovi);
            filmoviTable.setModel(tableModel);
        } catch (Exception e) {
            e.printStackTrace();
        }

        filmoviTable.addMouseListener(new MouseAdapter() {
            public void mousePressed(MouseEvent mouseEvent) {
                JTable table = (JTable) mouseEvent.getSource();
                Point point = mouseEvent.getPoint();
                int row = table.rowAtPoint(point);
                if (mouseEvent.getClickCount() == 2 && table.getSelectedRow() != -1) {
                    obrisiBtn.setEnabled(true);
                    Film film = (Film) tableModel.getValueAtRow(row);
                    selectedFilm = film;
                    movieInfoPanel.setVisible(true);
                    nazivInfo.setText(film.getNaziv());
                    zanrInfo.setText(film.getZanr());
                    trajanjeInfo.setText(film.getTrajanje() + " min");
                    cenaInfo.setText(film.getCena_karte() + " RSD");
                    reziserInfo.setText(film.getReziser());
                    glumciInfo.setText(film.getGlumci());
                    datumInfo.setText(film.getDatum_izlaska() + "");
                    opisInfo.setText(film.getOpis());
                }
            }
        });
    }

